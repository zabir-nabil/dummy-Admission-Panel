<h1>Khulna University of Engineering & Technology</h1>
<p style="color:blue;padding:15px;border:solid black 1px;">

Khulna University of Engineering & Technology (Bengali: খুলনা প্রকৌশল ও প্রযুক্তি বিশ্ববিদ্যালয়,
 pronounced: [kʰulna prokowʃɔl o prodʒukt̪i 
 biʃʃobid̪d̪alɔe̯]) commonly known as KUET 
 ([kue̯eʈ]; Bengali: কুয়েট), formerly BIT, 
 Khulna, is a public engineering university
 of Bangladesh emphasizing education and research
 on engineering and technology. This is
 a renowned university for the study of
 engineering in Bangladesh.

 <br/>
 <br/>
 Khulna University of Engineering & Technology (KUET),
 which started functioning from September 2003,
 is one of the five engineering universities of
 Bangladesh in higher education, research and
 development in engineering and technology.
 This university was established as Khulna
 Engineering College in 1974; the college
 was later converted to a degree-awarding
 autonomous institution called Bangladesh
 Institute of Technology (BIT), Khulna in
 July 1986 through a government ordinance
 (No. XXI, 1986).

The institute was upgraded and renamed as
 Khulna University of Engineering and Technology
 in September 2003 under the Khulna University
 of Engineering and Technology Act, 2003. KUET
 is financed by the government through the
 University Grants Commission. The university
 is an autonomous statutory organization of the
 Government of Bangladesh. There are statutory
 bodies like Syndicate, Academic Council, Finance
 Committee, Planning and Development Committee,
 Committee for Advance Studies and Research,
 Academic Committees, etc. for policy- and
 decision-making on aspects of the university
 under the frame work of the Act.
